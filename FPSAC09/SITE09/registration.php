<h2>Registration</h2>

<p>Registration is handled via the general <a href="/about/conferences/summer2009/">RISC Summer Registration</a>.</p>

<p>
For your convenience we offer to handle room reservation together with registration. Such 
rooms (good quality rooms with Internet access) will be booked at 
the Hotel Sommerhaus in Hagenberg, 5 minutes walking distance from
the conference venue. <br>
In case another person (who is not registered for a conference) comes along with you and also wants to stay in the Hotel Sommerhaus in Hagenberg, please note it at the registration, that we can make the right room reservations. 
</p>

<p style="color:red">
<b>Important note:</b> There is only a limited number of <b>double</b>
bedrooms in the Hotel Sommerhaus. In case you book that option, you will get a confirmation mail from
our secretaries (during office hours) whether or not a double bedroom
is indeed available.
</p>

<p>
 NOTE: These rooms at the Hotel Sommerhaus in Hagenberg will be 
      available by Saturday, July 18, from 2 p.m. on.
</p>
<p>
   The price is 34 Euro per person per night, including breakfast.
</p>
<p>
For those who prefer
   to stay in Linz, we refer to the list of <a href="http://www.google.com/search?q=hotels+in+linz&start=0&ie=utf-8&oe=utf-8&client=firefox-a&rls=org.mozilla:en-US:official">hotels in Linz</a> for individual booking.
   For commuting to Hagenberg, see
<a href="http://portal.risc.uni-linz.ac.at/about/map">the information on public transportation</a>. 
