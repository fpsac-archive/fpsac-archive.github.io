<h2>The Castle of Hagenberg &amp; Linz</h2>

The conference site is the Softwarepark Hagenberg centered around RISC. RISC, the
				    <a href="http://www.risc.uni-linz.ac.at/">Research Institute for Symbolic Computation</a>, is
 an institute of the
 <a href="http://www.jku.at/">Johannes Kepler University</a>
 in Linz. It is located in the
 <a href="http://www.risc.uni-linz.ac.at/about/castle/">Castle of Hagenberg</a>,
 a romantic medieval castle amidst the lovely, hilly landscape of the
 <a href="http://www.tiscover.at/at/guide/5,en,SCH1/objectId,RGN212296at,curr,EUR,season,at1,selectedEntry,home/home.html">M&uuml;hlviertel</a>
 20 km north east of
 <a href="http://en.wikipedia.org/wiki/Linz">Linz</a>,
 the regional capital of
 <a href="http://www.tiscover.at/upperaustria">Upper Austria</a>,
 located halfway between Salzburg and Vienna.</p>

<p>
 <a href="http://linz.at/english/">Linz is European Capital of Culture 2009.</a>
</p>

 <p>
 More information about RISC can be found on the institute <a href="http://www.risc.uni-linz.ac.at/">website</a> and in <a href="http://www.risc.uni-linz.ac.at/about/welcome/Folder_RISC.pdf">this folder</a>.


