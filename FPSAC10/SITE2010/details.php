  <a href="http://maven.smith.edu/~streinu/">Ileana Streinu</a> (Smith College, USA)
    <br><i>Rigidity, sparsity and pebble games</i> 
    <br>A famous open problem, going back to the work of James Clerk Maxwell in 1864, is to give a combinatorial characterization for generically rigid frameworks made from bars connected by rotatable joints. The same question can be asked for a long list of other geometrically constrained systems, but only very few answers are known. They include bar-and-joint frameworks in dimensions one and two, body-and-bar structures in arbitrary dimensions and a few other isolated instances such as skeleta of triangulated 3D polyhedra. The underlying combinatorial structure, in all these cases, is a graph satisfying some linear sparsity conditions. The pebble games are simple construction rules characterizing exactly those classes of sparse (hyper)graphs which are matroids. On the other hand, for Maxwell's problem the necessary (but not sufficient) sparsity condition falls "just below" the matroidal range.  
This talk will present these varied facets of combinatorial rigidity (accompanied by a variety of visual and physical props), and will conclude with some recent results. 
