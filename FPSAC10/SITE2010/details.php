  <a href="http://www.math.cornell.edu/~tsh/">Tara Holm</a> (Cornell University, USA)
    <br><i>The discrete geometry of moment polytopes</i> 
    <br>Moment polytopes are convex polytopes associated to Hamiltonian systems in symplectic geometry.  The discrete geometry of the moment polytope can tell us a good deal about the global geometry and topology of the corresponding system.  I will give a brief introduction to symplectic geometry to show how moment polytopes arise, followed by a survey demonstrating how combinatorial techniques can be brought to bear on topological questions. 
